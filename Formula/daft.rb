      url "https://github.com/avihut/daft/releases/download/v1.14.5/daft-x86_64-unknown-linux-gnu.tar.xz"
      sha256 "90fc756dbd1c1b51de31934d4220327fea62e57048b1bfd1ed865b77c8a07b07"
